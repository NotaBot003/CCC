while True:
    txt = input()
    if txt == "CU":
        print("see you")
    elif txt == ":-)":
        print("I'm happy")
    elif txt == ":-(":
        print("I'm unhappy")
    elif txt == ";-)":
        print("wink")
    elif txt == ":-p":
        print("stick out my tongue")
    elif txt == "(~.~)":
        print("sleepy")
    elif txt == "TA":
        print("totally awesome")
    elif txt == "CCC":
        print("Canadian Computing Competition")
    elif txt == "CUZ":
        print("because")
    elif txt == "TY":
        print("thank-you")
    elif txt == "YW":
        print("you're welcome")
    elif txt == "TTYL":
        print("talk to you later")
        break
    else:
        print(txt)
